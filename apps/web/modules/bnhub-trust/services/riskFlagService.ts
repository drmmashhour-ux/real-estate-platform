export async function listFlagsForAdmin(take = 100) {
  return prisma.bnhubTrustRiskFlag.findMany({
    orderBy: { createdAt: "desc" },
    take,
    include: {
      listing: { select: { id: true, title: true, city: true } },
    },
  });
}

export async function listSafeFlagsForHost(hostUserId: string) {
  const listings = await prisma.shortTermListing.findMany({
    where: { ownerId: hostUserId },
    select: { id: true },
  });
  const ids = listings.map((l) => l.id);
  return prisma.bnhubTrustRiskFlag.findMany({
    where: {
      listingId: { in: ids },
      visibilityScope: BnhubTrustRiskFlagVisibility.SAFE_HOST_VISIBLE,
      flagStatus: { in: [BnhubFraudFlagStatus.OPEN, BnhubFraudFlagStatus.UNDER_REVIEW] },
    },
    select: {
      id: true,
      listingId: true,
      flagType: true,
      summary: true,
      createdAt: true,
    },
  });
}

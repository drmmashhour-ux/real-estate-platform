import {
  BnhubFraudFlagStatus,
  BnhubFraudSeverity,
  BnhubTrustRiskFlagTypeV2,
  BnhubTrustRiskFlagVisibility,
} from "@prisma/client";
import { prisma } from "@/lib/db";
import { logRiskAction, logTrustAudit } from "@/modules/bnhub-trust/services/trustDecisionAuditService";
import { BnhubTrustIdentityAuditActor } from "@prisma/client";

export async function createRiskFlag(params: {
  listingId?: string | null;
  userId?: string | null;
  bookingId?: string | null;
  flagType: BnhubTrustRiskFlagTypeV2;
  severity: BnhubFraudSeverity;
  summary: string;
  evidence?: Record<string, unknown>;
  visibilityScope?: BnhubTrustRiskFlagVisibility;
  autoGenerated?: boolean;
  /** Dedupe: skip if open flag same type exists for listing */
  dedupeListing?: boolean;
}): Promise<{ id: string; created: boolean }> {
  if (params.dedupeListing && params.listingId) {
    const existing = await prisma.bnhubTrustRiskFlag.findFirst({
      where: {
        listingId: params.listingId,
        flagType: params.flagType,
        flagStatus: { in: [BnhubFraudFlagStatus.OPEN, BnhubFraudFlagStatus.UNDER_REVIEW] },
      },
    });
    if (existing) return { id: existing.id, created: false };
  }

  const row = await prisma.bnhubTrustRiskFlag.create({
    data: {
      listingId: params.listingId ?? undefined,
      userId: params.userId ?? undefined,
      bookingId: params.bookingId ?? undefined,
      flagType: params.flagType,
      severity: params.severity,
      visibilityScope: params.visibilityScope ?? BnhubTrustRiskFlagVisibility.ADMIN_ONLY,
      summary: params.summary,
      evidenceJson: (params.evidence ?? {}) as object,
      autoGenerated: params.autoGenerated ?? true,
    },
  });
  if (params.listingId) {
    await logRiskAction({
      actorType: BnhubTrustIdentityAuditActor.SYSTEM,
      listingId: params.listingId,
      actionType: "risk_flag_created",
      actionSummary: params.summary,
      after: { flagId: row.id, flagType: params.flagType },
    });
  }
  return { id: row.id, created: true };
}

export async function resolveRiskFlag(flagId: string, adminUserId: string) {
  const row = await prisma.bnhubTrustRiskFlag.update({
    where: { id: flagId },
    data: { flagStatus: BnhubFraudFlagStatus.RESOLVED },
  });
  await logTrustAudit({
    actorType: BnhubTrustIdentityAuditActor.ADMIN,
    actorId: adminUserId,
    entityType: "risk_flag",
    entityId: flagId,
    actionType: "resolved",
    actionSummary: `Flag resolved (listing ${row.listingId ?? "n/a"})`,
  });
}


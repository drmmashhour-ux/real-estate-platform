function opts(lang: ProjectsFilterLang, rows: Opt[]) {
  return rows.map((r) => ({ value: r.value, label: lang === "fr" ? r.fr : r.en }));
}

function cityOptions(lang: ProjectsFilterLang) {
  return [
    { value: "", label: lang === "fr" ? fr.allCities : en.allCities },
    { value: "Montreal", label: lang === "fr" ? fr.montreal : en.montreal },
    { value: "Laval", label: lang === "fr" ? fr.laval : en.laval },
  ];
}

export function getProjectsPageCopy(lang: ProjectsFilterLang) {
  const L = lang === "fr" ? fr : en;
  return {
    ...L,
    propertyTypeOptions: opts(lang, PROPERTY_TYPES),
    unitTypeOptions: opts(lang, UNIT_TYPES),
    roomsOptions: opts(lang, ROOMS),
    bathroomsOptions: opts(lang, BATHS),
    garageParkingOptions: opts(lang, GARAGE),
    plexOptions: opts(lang, PLEX),
    statusOptions: opts(lang, STATUSES),
    cityOptions: cityOptions(lang),
  };
}

